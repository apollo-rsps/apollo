/**
 * Registers the [FishingSpot] at the specified position.
 */
fun register(spot: FishingSpot, x: Int, y: Int, z: Int = 0) {
    val position = Position(x, y, z)
    Spawns.list.add(Spawn(spot.npc, "", position, Direction.NORTH))
}